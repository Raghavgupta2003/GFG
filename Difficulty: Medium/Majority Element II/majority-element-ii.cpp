class Solution {
  public:
    // Function to find the majority elements in the array
    vector<int> findMajority(vector<int>& arr) {
        // Your code goes here.
        unordered_map<int,int> m;
        for(auto it : arr){
            m[it]++;
        }
        
        int n = arr.size()/3;
        
        vector<int> v;
        for(auto pair : m){
            if(pair.second>n) v.push_back(pair.first);
        }
        
        sort(v.begin(),v.end());
        return v;
    }
};

